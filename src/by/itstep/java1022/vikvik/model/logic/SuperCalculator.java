package by.itstep.java1022.vikvik.model.logic;

public class SuperCalculator {
    public SuperCalculator() {
    }

    public int add(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int mul(int a, int b) {
        return a * b;
    }

    public int div(int a, int b) {
        return a / b;
    }
}
